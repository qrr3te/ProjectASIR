            <h2>1. ¿Qué son las cookies?</h2>
            <p>
                Las cookies son pequeños archivos de texto que se almacenan en tu dispositivo (ordenador, tableta o móvil) 
                cuando visitas un sitio web. Estas cookies permiten que la web reconozca tu dispositivo y almacene cierta información 
                sobre tus preferencias o actividades previas en el sitio.
            </p>
        </section>

        <section>
            <h2>2. ¿Qué tipos de cookies utilizamos?</h2>
            <p>En nuestra web utilizamos los siguientes tipos de cookies:</p>
            <ul>
                <li>
                    <strong>Cookies esenciales:</strong> Estas cookies son necesarias para el funcionamiento básico del sitio web 
                    y no pueden ser desactivadas en nuestros sistemas. Sin ellas, el sitio web no funcionará correctamente.
                </li>
                <li>
                    <strong>Cookies de rendimiento:</strong> Estas cookies recopilan información sobre cómo los usuarios interactúan 
                    con nuestra web, como las páginas que visitan con más frecuencia. Toda la información que recogen es anónima.
                </li>
                <li>
                    <strong>Cookies de funcionalidad:</strong> Estas cookies permiten que el sitio recuerde tus elecciones (como el idioma o 
                    la región en la que te encuentras) para ofrecerte una experiencia personalizada.
                </li>
                <li>
                    <strong>Cookies de marketing:</strong> Estas cookies se utilizan para ofrecer anuncios relevantes y medir su eficacia. 
                    También pueden usarse para rastrear tus actividades en diferentes sitios web.
                </li>
            </ul>
        </section>

